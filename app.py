def create_pdf(df, prod_qty, selected_month):
    pdf = FPDF()
    pdf.add_page()
    pdf.set_font("Arial", 'B', 16)
    
    # Header
    pdf.cell(190, 10, f"AL-SIDRA UTILITIES REPORT - {selected_month}", ln=True, align='C')
    pdf.set_font("Arial", '', 12)
    pdf.cell(190, 10, "Maintenance Department (Utilities Section)", ln=True, align='C')
    pdf.ln(10)
    
    # Metrics Table
    pdf.set_fill_color(240, 240, 240)
    pdf.cell(95, 10, "Metric Description", 1, 0, 'C', True)
    pdf.cell(95, 10, "Value", 1, 1, 'C', True)
    
    avg_prod = prod_qty / 30
    metrics = [
        ("Total Electricity Consumption", f"{df['ELEC'].sum():,.0f} kWh"),
        ("Total LPG Consumption", f"{df['LPG'].sum():,.0f} kg"),
        ("Total Water Intake", f"{df['W_IN'].sum():,.0f} m3"),
        ("Electricity Efficiency", f"{(df['ELEC'].mean()/avg_prod):.3f} kWh/kg"),
        ("LPG Efficiency", f"{(df['LPG'].mean()/avg_prod):.4f} kg/kg"),
        ("Water Loss Percentage", f"{((df['W_IN'].sum()-df['W_OUT'].sum())/df['W_IN'].sum()*100):.1f}%")
    ]
    
    for m, v in metrics:
        pdf.cell(95, 10, m, 1)
        pdf.cell(95, 10, v, 1, 1, 'C')
        
    pdf.ln(10)
    pdf.set_font("Arial", 'I', 10)
    pdf.cell(190, 10, "Generated by Al-Sidra Intelligence App - 2025", align='C')
    
    return pdf.output(dest='S').encode('latin-1')

